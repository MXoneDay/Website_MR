    <h2>Race</h2>
    <h4>Formation Lap</h4>
    <p class="mb-4 tm-intro-text">
        Een Formation lap in F1 is bedoeld om je banden op te warmen.
        Doordat de een met minder assists aanrijd dan de ander kan het veel uit maken of de banden zijn opgewarmt.
        Daarom willen wij niet dat er word ingehaald gedurende de Formation Lap ook al rijd de persoon voor jou heel sloom.
        Daarop volgende Sloom rijden en inhalen word eerst beoordeeld als een waarschuwing.
        Doe je dit herhaaldelijk is dit een overtreding van de regels en kan dit bestraft worden.
    </p>
    <h4>De Start</h4>
    <p class="mb-4 tm-intro-text">
        De start is waarschijnlijk voor velen een van de spannendste momenten uit de race.
        Dit omdat iedereen kort op elkaar zit en er vaak een lang recht stuk gevolgd word met een scherpe bocht naar links of rechts.
        Vaak zijn er een aantal te overmoedig aan het begin van een race, die zichzelf en andere gelijk op een achterstand zetten.
        Dit is echter te voorkomen door iets eerder te remmen een 50% race duurt gemiddeld drie kwartier tijd zat om iemand in te halen.
        Soms moet je even je verlies nemen voor een bocht om het later goed te kunnen proberen. Zo voorkom je penalties en houd je het ook leuk voor anderen
        Vaak kan er iets gebeuren in bocht 1 en grote gevolgen hebben, Belangrijk is dat je nooit op geeft je weet nooit wat er verder gaat gebeuren!
    </p>
